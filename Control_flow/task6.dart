// Task 6: Write a for loop that iterates from 1 to 20. Inside the loop, use an if statement 
// to print only the numbers that are multiples of 3.
// Student: Muhammad Umar Toshpo'latov
// University: New Uzbekistan University

void main() {
  for (int i = 1; i <= 20; i++) {
    if (i % 3 == 0) {
      print(i);
    }
  }
}
